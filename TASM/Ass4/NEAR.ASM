PRINT MACRO	STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM

PRINTCH MACRO	
MOV AH,02H
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	STR1 DB 20 DUP('$')
	NEWLINE DB	10,13,'$'
.CODE
	MOV	AX,@DATA
	MOV	DS,AX
	
	CALL ACCEPT
	
	PRINT NEWLINE
	PRINT STR1+2
	PRINT NEWLINE
	
	MOV DL,STR1+1
	ADD DL,'0'
	PRINTCH
	
	FINISH

	ACCEPT PROC NEAR
	LEA DX,STR1
	MOV AH,0AH
	INT 21H
	RET
	ACCEPT ENDP
END
