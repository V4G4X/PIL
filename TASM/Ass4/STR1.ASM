FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	STR1	DB	20 DUP('$')
	NEWLINE	DB	10,13,'$'
	PUBLIC STR1
.CODE
	MOV AX,@DATA
	MOV DS,AX
	
	CALL ACCEPT
	
	LEA DX,NEWLINE
	MOV AH,09H
	INT 21H
	
	CALL PRINT
	
	FINISH
	
	PRINT PROC
	MOV DX,OFFSET STR1
	MOV AH,09H
	INT 21H
	RET
	PRINT ENDP
	
	ACCEPT PROC
	MOV DX,OFFSET STR1
	MOV AH,3FH
	INT 21H
	RET
	ACCEPT ENDP
END
