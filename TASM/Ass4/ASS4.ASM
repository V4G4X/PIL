PRINTSTR MACRO STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM

SCANCH	MACRO	VAR
MOV AH,01H
INT 21H
MOV VAR,AL
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	CHOICE	DB	00H
	STR1	DB	'1. CONCATENATE',10,13,'$'
	STR2	DB	'2. COMPARE',10,13,'$'
	STR3	DB	'3. EXIT',10,13,'$'
	STR4	DB	10,13,'ENTER PROPER CHOICE',10,13,'$'
	NEWLINE	DB	10,13,'$'
	
.CODE
EXTRN CONCAT:FAR
EXTRN COMPARE:FAR

	MOV AX,@DATA
	MOV DS,AX

MENU:
	PRINTSTR STR1
	PRINTSTR STR2
	PRINTSTR STR3
	
	SCANCH CHOICE
	SUB CHOICE,30H
	
	CMP CHOICE,01H
	JE CALL_CONCAT

	CMP CHOICE,02H
	JE CALL_COMPARE

	CMP CHOICE,03H
	JE FINISH_CALL

	JMP DEFAULT
	
CALL_CONCAT:
	PRINTSTR NEWLINE
	CALL CONCAT
	PRINTSTR NEWLINE
	JMP 	MENU
	
CALL_COMPARE:
	PRINTSTR NEWLINE
	CALL COMPARE
	PRINTSTR NEWLINE
	JMP 	MENU
	
DEFAULT:
	PRINTSTR STR4
	JMP MENU
	
FINISH_CALL:
	FINISH
END
