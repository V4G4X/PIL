PRINTSTR MACRO STR
LEA DX,STR
MOV AH,9H
INT 21H
ENDM

PRINT_C MACRO
MOV AH,02H
INT 21H
ENDM

SCAN_C MACRO			;STORES RESULT IN AL
MOV AH,01H
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	MSG1 DB 'ENTER 10 ELEMENTS',10,13,'$'
	MSG2 DB 10,13,'NOW PRINTING SUM',10,13,'$'
	SUM DB 0
	ARR DB ?
.CODE
	MOV AX,@DATA
	MOV DS,AX

	MOV CX,10
	LEA SI,ARR

	PRINTSTR MSG1

	L1:			;LOOP TO ACCEPT ARRAY
		SCAN_C
		MOV [SI],AL
		INC SI
		DEC CX
		JNZ L1
	
	PRINTSTR MSG2
	
	MOV CX,10
	LEA SI,ARR
	
	FINISH
	
	END
