PRINT_C MACRO
MOV AH,02H
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	A	DB	0
	B	DB	0
	SUM	DB	80H
.CODE
	MOV AX,@DATA
	MOV DS,AX

	MOV AL,SUM
	MOV CL,04H
	
	ROL AL,CL
	
	AND AL,0FH
	ADD AL,'0'
	MOV DL,AL
	PRINT_C
	
	MOV AL,SUM
	
	AND AL,0FH
	ADD AL,'0'
	MOV DL,AL
	PRINT_C

	FINISH 
END
