PRINTSTR MACRO STR
LEA DX,STR
MOV AH,9H
INT 21H
ENDM

PRINT_C MACRO
MOV AH,02H
INT 21H
ENDM

SCAN_C MACRO			;STORES RESULT IN AL
MOV AH,01H
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	MSG1 DB 'ENTER 10 ELEMENTS',10,13,'$'
	MSG2 DB 10,13,'NOW PRINTING',10,13,'$'
	NL DB 10,13,'$'
	ARR DB ?
.CODE
	MOV AX,@DATA
	MOV DS,AX

	PRINTSTR MSG1

	LEA SI,ARR
	MOV CX,10
	L1:			;LOOP TO ACCEPT ARRAY
		SCAN_C
		PRINTSTR NL
		MOV [SI],AL
		INC SI
		DEC CX
		JNZ L1
	
	PRINTSTR MSG2
	
	MOV CX,10
	LEA SI,ARR

	L2:			;LOOP TO PRINT ARRAY
		MOV DL,[SI]	
		PRINT_C
		PRINTSTR NL
		INC SI
		LOOP L2
	
	FINISH
	
	END
