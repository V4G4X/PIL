PRINT	MACRO	STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM

ACCEPT_STR	MACRO	STR
LEA DX,STR
MOV AH,0AH
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	STR1	DB	20 DUP('$')
	L1	DB	0
	STR2	DB	20 DUP('$')
	L2	DB	0
	UNEQ	DB	'THE STRINGS ARE UNEQUAL',10,13,'$'
	EQUAL	DB	'THE STRINGS ARE EQUAL',10,13,'$'
	NEWLINE	DB	10,13,'$'
.CODE
	MOV AX,@DATA
	MOV DS,AX
	
	ACCEPT_STR STR1
	MOV BH,STR1+1
	MOV [L1],BH
	
	PRINT NEWLINE
	
	ACCEPT_STR STR2
	MOV BH,STR2+1
	MOV [L2],BH

	PRINT NEWLINE

	MOV CH,L1
	MOV CL,L2
	CMP CH,CL
	JNE UNEQUAL
	
	MOV CH,0
	MOV SI,0
	
	L:
		MOV AH,[STR1+SI]
		MOV AL,[STR2+SI]
		CMP AH,AL
		JNE UNEQUAL
		INC SI
		CMP SI,	CX
		JNE L
	PRINT EQUAL
	FINISH
	
	UNEQUAL:
		PRINT	UNEQ
		FINISH
END
