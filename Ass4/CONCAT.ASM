PRINT	MACRO	STR
LEA DX,STR
MOV AH,09H
INT 21H
ENDM

ACCEPT_STR	MACRO	STR
LEA DX,STR
MOV AH,0AH
INT 21H
ENDM

FINISH MACRO
MOV AH,4CH
INT 21H
ENDM

.MODEL SMALL
.DATA
	STR1	DB	20 DUP('$')
	L1	DB	0
	STR2	DB	20 DUP('$')
	L2	DB	0
	STR3	DB	20 DUP('$')
	S1	DB	'ENTER FIRST STRING',10,13,'$'
	S2	DB	'ENTER SECOND STRING',10,13,'$'
	NEWLINE	DB	10,13,'$'
.CODE
	MOV AX,@DATA
	MOV DS,AX
	
	PRINT S1
	
	ACCEPT_STR STR1
	MOV BH,STR1+1
	MOV [L1],BH
	PRINT NEWLINE
	
	PRINT S2
	
	ACCEPT_STR STR2
	MOV BH,STR2+1
	MOV [L2],BH
	
	PRINT NEWLINE
	
	MOV SI,2
	MOV BH,0
	MOV BL,[L1]
	ADD BL,2
	
	MOV CH,0
	MOV CL,[L2]
	ADD CL,BL
	
	LP1:
		MOV AL,STR1[SI]
		MOV STR3[SI],AL
		INC SI
		CMP SI,BX
		JNE LP1
	MOV DI,2
	LP2:
		MOV AL,STR2[DI]
		MOV STR3[SI],AL
		INC SI
		INC DI
		CMP SI,CX
		JNE LP2
	SUB CL,2
	MOV STR3[1],CL
		
	PRINT STR3+2
	FINISH
END
