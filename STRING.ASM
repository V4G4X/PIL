PRINT MACRO MSG
LEA DX,MSG
MOV AH,9H
INT 21H
ENDM
	
SCAN MACRO MSG
LEA DX,MSG
MOV AH,3FH
INT 21H
ENDM
	
FINISH MACRO
MOV AH,4CH
INT 21H
ENDM
	
